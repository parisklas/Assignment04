//STEP 1
function halfNumber(num) {
    return console.log(`Half of ${num} is ${num / 2}`);
}
halfNumber(9);

//STEP 2
function squareNumber(numSq) {
    return console.log(`The result of squaring the number ${numSq} is ${numSq * numSq}`);
}
squareNumber(36);

//STEP 3
function percentOf(x, y) {
    return console.log(`${x} is ${(x / y) * 100}% of ${y}`);
}
percentOf(8,4);
//STEP 4
function findModulus(num1, num2) {
    return console.log(`${num1%num2} is the modulus of ${num1} and ${num2}`);
}
findModulus(10,4);